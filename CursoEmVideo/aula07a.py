n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
soma = n1 + n2
divisao = n1 / n2
mult = n1 * n2
potencia = n1 ** n2
divInt = n1 // n2
print('A soma é {} e o produto é {} a potencia é {}'.format(soma, mult, potencia))
print('A divisão é {} e a divisão inteira é {}'.format(divisao, divInt))
